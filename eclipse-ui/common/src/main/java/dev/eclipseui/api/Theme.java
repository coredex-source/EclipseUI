package dev.eclipseui.api;

/**
 * Available visual themes for EclipseUI config screens.
 */
public enum Theme {
    /**
     * Faithful to vanilla aesthetics with vanilla-style buttons and controls.
     */
    FAITHFUL,
    
    /**
     * Modern theme inspired by Sodium - dark with cyan accents.
     */
    MODERN,
    
    /**
     * Custom theme using user-provided ThemeData.
     */
    CUSTOM
}
